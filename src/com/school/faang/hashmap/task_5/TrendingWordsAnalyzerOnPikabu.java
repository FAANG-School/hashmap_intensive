package com.school.faang.hashmap.task_5;

import java.util.HashMap;
import java.util.Map;

public class TrendingWordsAnalyzerOnPikabu {

    public static void main(String[] args) {
        System.out.println("Trending Words Analyzer On Pikabu:");

        String text = "В сфере IT есть разные направления разработки, вот самые распространенные:\n" +
                "Веб-разработка — это разработка сервисов и инструментов, которыми мы пользуемся в интернете;\n" +
                "Системная разработка — разработка программ, с помощью которых компьютер выполняет задачи: операционных систем, драйверов;\n" +
                "Десктоп-разработка — разработка программ для запуска под управлением операционной системы;\n" +
                "Геймдев — разработка игр;\n" +
                "Мобильная разработка — разработка приложений для телефонов и планшетов;\n" +
                "Data Science и анализ данных — хранение, обработка и анализ больших объемов данных.\n" +
                "Веб-разработка\n" +
                "\n" +
                "Мир веб-разработки — если говорить только о программистах без околоайтишных специальностей — делится на две категории: фронтенд и бэкенд. Фронтенд (англ. frontend — «внешняя часть») — это всё, что видит пользователь на экране или чего касается курсором мыши, например кнопка на сайте. Когда пользователь нажимает на кнопку, в игру вступает бэкенд (англ. backend — «внутренняя часть»). Его задача — организовать взаимодействие с базами данных и другими серверами, настроить поиск нужной информации и отправить её обратно пользователю.\n" +
                "Фронтенд – визуальная сторона, бэкенд – все, что под капотом.\n" +
                "Фронтенд-разработчики создают интерфейс для взаимодействия с приложением, задают структуру веб-страницы на языке разметки HTML, определяют её внешний вид с помощью CSS, а на языке программирования JavaScript (JS) добавляют динамический контент, например всплывающие окна. Помимо JavaScript используют язык TypeScript, расширяющий возможности JavaScript, и фреймворки: React, Vue или Angular.  \n" +
                "\n" +
                "Двадцать лет назад, чтобы называть себя полноценным фронтендером, достаточно было знать HTML и CSS. Сегодня из-за обилия фреймворков и технологий фронтенд по сложности догнал бэкенд.\n" +
                "\n" +
                "Бэкенд-разработчики отвечают за логику работы сервера: сохранение данных в базу, хранение файлов, механика авторизации. Бэкенд-разработчики пишут на разных языках программирования: JavaScript, C#, Go, PHP, Python, Java.\n" +
                "\n" +
                "В 2009 году появилась среда Node.js, позволяющая разрабатывать бэкенд на JavaScript. Зная один язык, программист способен выполнять задачи как фронтенда, так и бэкенда. Такой специалист-универсал называется фулстек-разработчиком.\n" +
                "\n" +
                "Давайте разберёмся в преимуществах и недостатках каждого языка для начинающих, сравним зарплаты и выберем язык для старта, чтобы попробовать себя в веб-разработке.";

        String processedText = text.toLowerCase();

        processedText = processedText.replaceAll("[^a-zA-Zа-яА-Я\\s]", "");

        String[] words = processedText.split("\\s+");

        Map<String, Integer> wordFrequency = new HashMap<>();

        for (String word : words) {
            if (word.isEmpty()) {
                continue;
            }
            wordFrequency.put(word, wordFrequency.getOrDefault(word, 0) + 1);
        }

        boolean foundTrendingWords = false;
        for (Map.Entry<String, Integer> entry : wordFrequency.entrySet()) {
            String word = entry.getKey();
            Integer frequency = entry.getValue();

            if (frequency > 5) {
                System.out.println("Word: '" + word + "', Frequency: " + frequency);
                foundTrendingWords = true;
            }
        }

        if (!foundTrendingWords) {
            System.out.println("There are no words with a frequency greater than 5.");
        }
    }
}
